export const getRandomCards = (count: number): BudgetCard[] => {
  const shuffled = [...budgetCards].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

export const getCardsByCategory = (category: BudgetCard['category']): BudgetCard[] => {
  return budgetCards.filter(card => card.category === category);
};
